public class java06 {
    public static void main(String[] args) {
        //sequencia de escape

        //aspas duplas = \"
        System.out.println("\"hello, word!!!\"");
        // nova linha \n
        // retorno de carro = coloca cursor abaixo
        System.out.print("hello, word \n agora vamos codar nessa outra linha \r");
        //colocar \\ para ficar com a barra invertida tipo fração
        System.out.println("1\\4 de diametro da peça");
    }
}
